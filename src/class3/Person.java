package class3;

public class Person {
	String name,number;
	
	public Person(String na,String nu){
		this.name = na;
		this.number = nu;
	}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}
	
}
